<?php


namespace App\Intermediary;


final class IntermediaryObject {

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name): void
    {
        $this->name = $name;
    }

    private string $name;

}